    private boolean addCustomer() throws SQLException {
        String name = tfFullName.getText();
        String tel = tfTelNum.getText();
        String email = tfEmail.getText();

        // Kiem tra dieu kien
        //
        Customer customer = new Customer(name, tel, email);
        boolean result = pc.addCustomerController(customer);
        return result;
    }

