    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            if (check()) {
                if (addCustomer()) {
                    JOptionPane.showMessageDialog(this, "Added Customer'Info to DB");
                }
                double totalAmount = 0.0;
                for (Product list1 : list) {
                    if (list1.isBool()) {
                        totalAmount += list1.getPrice() * list1.getQts();
                    }
                }
                JOptionPane.showMessageDialog(this, "Calculated!");
                tfTotalAmount.setText(Double.toString(totalAmount));
            }
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(OrderAdministration.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

